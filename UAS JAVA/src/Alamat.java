public class Alamat {
    private String negara;
    private String kota;

    public String getNegara() {
        return this.negara;
    }
    public void setNegara(String negara) {
        this.negara = negara;
    }

    public String getKota() {
        return this.kota;
    }
    public void setKota(String kota) {
        this.kota = kota;
    }
}
